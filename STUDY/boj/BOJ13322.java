package boj;
import java.util.*;

public class BOJ13322 { // 접두사 배열

    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);
        String str = sc.next();

        for (int i = 0; i < str.length(); i++) {
            System.out.println(i);
        }

        sc.close();
    }
}